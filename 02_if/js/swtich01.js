let code = "HTML";

switch(code){
    case "HTML":
        console.log("拡張子は.htmlです");
        break;
    case "CSS":
        console.log("拡張子は.cssです");
        break;
    case "JavaScript":
        console.log("拡張子は.jsです");
        break;
    default:
        console.log("分からないファイルです");
        break;
}